package com.calculator.controller;

public class Calculator {
    public static int addCalculator(int a, int b) {
        return a + b;
    }

    public static int subCalculator(int a, int b) {
        return a - b;
    }

    public static int mulCalculator(int a, int b) {
        return a * b;
    }

    public static int divCalculator(int a, int b) {
        return a / b;
    }
}
