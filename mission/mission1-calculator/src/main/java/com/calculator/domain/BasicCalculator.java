package com.calculator.domain;

public class BasicCalculator {

    public int getAdd(int a, int b){

        return a + b;
    }

    public int getMinus(int a, int b){

        return a - b;
    }

    public int getMultiply(int a, int b){

        return a * b;
    }

    public int getDivide(int a, int b){

        return a / b;
    }
}
