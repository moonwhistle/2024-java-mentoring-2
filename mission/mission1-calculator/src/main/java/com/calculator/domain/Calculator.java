package com.calculator.domain;

public class Calculator {

    public int addCalculator(int a, int b) {
        return (a + b);
    }

    public int subCalculator(int a, int b) {
        return (a - b);
    }

    public int mulCalculator(int a, int b) {
        return (a * b);
    }

    public int divCalculator(int a, int b) {
        return (a / b);
    }
}
